package it.yourcompany.GestioneBiblioteca;

public class Lettore {
    int id;
    String nome;
    String cognome;
    String email;

    public Lettore(int id, String nome, String cognome, String email) {
        this.id = id;
        this.nome = nome;
        this.cognome = cognome;
        this.email = email;
    }
}
